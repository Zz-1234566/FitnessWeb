CREATE TABLE `food_item` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
  `name` VARCHAR(128) NOT NULL COMMENT '食物名称',
  `imageUrl` VARCHAR(1024) DEFAULT NULL COMMENT '食物图片URL',
  `category` VARCHAR(64) DEFAULT NULL COMMENT '分类',
  `unit` VARCHAR(32) NOT NULL DEFAULT 'g' COMMENT '单位',
  `baseAmount` DECIMAL(10,2) NOT NULL DEFAULT 100.00 COMMENT '营养基准量',
  `calories` DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '热量(kcal)',
  `protein` DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '蛋白质(g)',
  `carbs` DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '碳水(g)',
  `fat` DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '脂肪(g)',
  `fiber` DECIMAL(10,2) NOT NULL DEFAULT 0.00 COMMENT '膳食纤维(g)',
  `createdBy` BIGINT DEFAULT NULL COMMENT '创建人',
  `isSystem` TINYINT NOT NULL DEFAULT 0 COMMENT '是否系统食物',
  `isDelete` TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除',
  `createTime` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updateTime` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_name` (`name`),
  KEY `idx_createdBy` (`createdBy`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='食物营养表';
